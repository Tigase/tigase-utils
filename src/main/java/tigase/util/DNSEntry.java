public class DNSEntry {

	private int port = 5269;
	private String ip = null;

	public DNSEntry(int port, String ip) {
		this.port = port;
		this.ip = ip;
	}

	public DNSEntry(String ip) {
		this.ip = ip;
	}

	public String getIp() {
		return ip;
	}

	public int getPort() {
		return port;
	}

	@Override
	public String toString() {
		return "port: " + port + ", ip: " + ip;
	}

}
